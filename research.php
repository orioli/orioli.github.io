<p>My point of view is that science has to serve for human life. During 6 years I did my research in the pure mathematics
field and I have got some interesting results in complex and functional analysis, for example, I have described analogues
of Privalov classes in the upper half-plane, found factorizations for functions from these classes, etc. You can download
some articles in "Publications" section of this site if you are interested in this field.</p>
<p>For sure, these results can be applied in some fields related to Fourier transform, but all the time during my research work
I was thinking: Why am I doing it? What is the purpose in my research activiy? What can I reach in my researches? I have read many
books about such great mathematicians as A.N.Kolmogorov, A.M.Lyapunov, and others. I tried to analyze how did they create their
discoveries. And again and again I came to the point that all their discoveries were related to problems in non-mathematical fields.
I understood that to be good researcher means to be specialists in different fields. And it was turning point in my research activity.</p>
<p>I started to find interdisciplinary problems, and it was lucky coincidence that I have contacted Dr.Philip Maini from the Centre of
Mathematical Biology in the Oxford University. He introduced me wonderful world of Mathematical Biology. I started reading different books
and papers in different directions of Mathematical Biology, and it was really phenomenal information for me. I read about cells, DNA, RNA,
proteins,  interactions between it... And I opened for myself many possibilities to apply my mathematical background for solving
biological problems. I realized that this is my field and I can get many interesting results in this area.</p>
<p>It turned out that it was true. Now my research is devoted to the protein complexes prediction based on protein-protein interactions
networks.</p>
